    public Config(Manhunt plugin) {
        this.plugin = plugin;
        file = new File(plugin.getDataFolder(), "config.yml");
        if (!file.exists()) {
            plugin.saveResource("config.yml", false);
        }
        plugin.getConfig().options().copyDefaults(true);
        plugin.saveDefaultConfig();
        plugin.saveConfig();
        plugin.reloadConfig();

        load();
        save();
    }

